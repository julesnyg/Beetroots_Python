Number_1 = 23
Number_2 = 65

'Addition'
print(Number_1 + Number_2)
'Multiplication'
print(Number_1 * Number_2)
'Subtraction'
print(Number_2 - Number_1)
'Division'
print(Number_2 / Number_1)
'Modulus is going to give us the remainder after the last divisible factor without decimals'
print(Number_2 % Number_1)
'Exponent'
print(Number_2 ** Number_1)
'Floor division is going to give us the rounded result to the nearest whole number without decimal'
print(Number_2 // Number_1)